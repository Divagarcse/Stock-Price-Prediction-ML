"""
This module creates technical indicators and
machine learning features for stock price prediction.
"""

import ta
import pandas as pd


def add_technical_indicators(df):
    """
    Create technical indicators.
    """

    # Simple Moving Average (5 Days)
    df["SMA_5"] = ta.trend.sma_indicator(
        df["Close"],
        window=5
    )

    # Simple Moving Average (20 Days)
    df["SMA_20"] = ta.trend.sma_indicator(
        df["Close"],
        window=20
    )

    # Exponential Moving Average (10 Days)
    df["EMA_10"] = ta.trend.ema_indicator(
        df["Close"],
        window=10
    )

    # Relative Strength Index
    df["RSI"] = ta.momentum.rsi(
        df["Close"],
        window=14
    )

    # MACD
    df["MACD"] = ta.trend.macd(
        df["Close"]
    )

    print("Technical Indicators Created Successfully")

    return df


def create_lag_features(df):
    """
    Create lag features.
    """

    df["Lag_1"] = df["Close"].shift(1)
    df["Lag_2"] = df["Close"].shift(2)
    df["Lag_3"] = df["Close"].shift(3)

    print("Lag Features Created Successfully")

    return df


def create_daily_return(df):
    """
    Create daily return feature.
    """

    df["Daily_Return"] = df["Close"].pct_change()

    print("Daily Return Created Successfully")

    return df


def create_target(df):
    """
    Create target variable.
    """

    df["Target"] = df["Close"].shift(-1)

    print("Target Variable Created Successfully")

    return df


def remove_missing_values(df):
    """
    Remove missing values generated by feature engineering.
    """

    print("\nMissing Values Before Cleaning\n")
    print(df.isnull().sum())

    df.dropna(inplace=True)
    df.reset_index(drop=True, inplace=True)

    print("\nMissing Values After Cleaning\n")
    print(df.isnull().sum())

    return df


if __name__ == "__main__":

    from data_preprocessing import load_data, preprocess_data

    DATA_PATH = "data/raw/aapl.us.txt"

    df = load_data(DATA_PATH)

    df = preprocess_data(df)

    df = add_technical_indicators(df)

    df = create_lag_features(df)

    df = create_daily_return(df)

    df = create_target(df)

    df = remove_missing_values(df)

    print("\nFinal Dataset Shape:", df.shape)

    print(df.head())